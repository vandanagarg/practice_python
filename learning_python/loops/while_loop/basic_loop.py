#while loop
# a structure in python which allows us to loop through and execute a block of code a multiple times .

#basic loop

i = 1
while i <= 10: #while condition , so till the condition next to while is met or is true the line of codes will keep on executing inside the while loop
    print(i)
    i = i + 1  #or we can write i += 1

print("done with the loop")
